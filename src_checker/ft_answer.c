#include "libps.h"
#include <unistd.h>

void	ft_answer(t_stack **a, t_stack **b)
{
	if (ft_issorted(a) && *b == NULL)
		write(1, "OK\n", 3);
	else
		write(1, "KO\n", 3);
}
